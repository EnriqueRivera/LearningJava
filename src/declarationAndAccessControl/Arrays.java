
package declarationAndAccessControl;


public class Arrays {
    public static void main(String[] args) {
        
        //Declaring an Array of Primitives
        int[] key;      // Square brackets before name (recommended)
        int key2 [];    // Square brackets after name (legal but less readable)
        
        //Declaring an Array of Object References
        Thread[] threads;       // Recommended
        Thread threads2 [];     // Legal but less readable
    }
}
